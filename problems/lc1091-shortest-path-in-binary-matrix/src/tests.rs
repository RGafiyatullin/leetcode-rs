use crate::Solution;

fn run_one_case(case: (&[&[i32]], i32)) {
    let (grid, expected) = case;
    let actual =
        Solution::shortest_path_binary_matrix(grid.iter().map(|row| row.to_vec()).collect());
    assert_eq!(actual, expected, "grid: {:?}", grid);
}

#[test]
fn run_all_cases() {
    for case in CASES {
        run_one_case(*case);
    }
}

