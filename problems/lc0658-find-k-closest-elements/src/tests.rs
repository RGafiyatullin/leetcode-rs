fn run_all_cases() {
    for (id, (sorted, k, x, expected)) in CASES.iter().copied().enumerate() {
        eprintln!("### {:?}", id);
        eprintln!("a: {:?}", &sorted[0..std::cmp::min(sorted.len(), 20)]);
        eprintln!("k: {:?}, x: {:?}", k, x);
        let actual =
            crate::window_and_bisect::Solution::find_closest_elements(sorted.to_vec(), k, x);

        if actual != expected {
            eprintln!("exp.len: {:?}; act.len: {:?}", expected.len(), actual.len());
            expected.chunks(10).zip(actual.chunks(10)).enumerate().for_each(
                |(chunk_idx, (exp, act))| {
                    eprintln!(
                        "{}\t{:?} | {:?} | {:?}",
                        if exp == act { "[OK]" } else { "[ERR]" },
                        chunk_idx * 10,
                        exp,
                        act
                    );
                },
            );
            assert!(false, "{:?} | {:?}, {:?}", sorted, k, x)
        }
    }
}

