pub struct Solution;

impl Solution {
    pub fn two_sum(mut numbers: Vec<i32>, target: i32) -> Vec<i32> {
        assert!(numbers.len() >= 2);
        assert!(numbers.len() <= 30000);

        numbers.sort();

        let (right_idx, left_idx) = numbers
            .iter()
            .copied()
            .enumerate()
            .flat_map(|(left_idx, left_val)| {
                let complement = target - left_val;
                index_of(&numbers[..], left_idx + 1, complement).zip(Some(left_idx))
            })
            .next()
            .expect("The tests are generated such that there is exactly one solution.");

        assert!(left_idx < right_idx, "{} < {}", left_idx, right_idx);

        vec![left_idx as i32 + 1, right_idx as i32 + 1]
    }
}

fn index_of(slice: &[i32], starting_from_index: usize, target: i32) -> Option<usize> {
    use std::cmp::Ordering::*;

    if slice[starting_from_index..].is_empty() {
        return None
    }

    let (mut lo, mut hi) = (starting_from_index, slice.len() - 1);

    loop {
        let idx = (lo + hi) / 2;
        let val = slice[idx];
        let left_neighbour_val =
            slice.get(idx - 1).filter(|_| idx - 1 >= starting_from_index).copied();

        // std::thread::sleep(std::time::Duration::from_millis(100));
        // eprintln!(" | {:?}<={:?}<={:?}", lo, idx, hi);
        // eprintln!("   | val={:?}, left={:?}", val, left_neighbour_val);

        match (lo == hi, val.cmp(&target), left_neighbour_val.map(|v| v.cmp(&target))) {
            (false, Less, _) => lo = idx + 1,
            (false, Greater, _) => hi = idx,
            (_, Equal, Some(Less) | None) => break Some(idx),
            (_, Equal, _) => hi = idx,
            (true, Less | Greater, _) => break None,
        }
    }
}

